from ollama import chat

from config import MODEL


def extract_fact(user_text):
    response = chat(
        model=MODEL,
        messages=[
            {
                "role": "system",
                "content": """
Ты анализатор памяти.

Нужно сохранять ТОЛЬКО долговременную информацию о пользователе.

Сохраняй:
- имя
- возраст
- профессию
- увлечения
- предпочтения
- домашних животных
- важные жизненные факты
- долгосрочные привычки

НЕ сохраняй:
- текущие действия
- временные состояния
- случайные фразы
- шутки
- вопросы
- разовые события

Примеры:

Сообщение:
Я люблю шахматы

Ответ:
Пользователь любит шахматы

Сообщение:
У меня есть собака Рекс

Ответ:
У пользователя есть собака Рекс

Сообщение:
Я лежу вверх ногами

Ответ:
NONE

Сообщение:
Я хожу боком

Ответ:
NONE

Сообщение:
Я пью кофе каждое утро

Ответ:
Пользователь пьет кофе каждое утро

Если полезного долгосрочного факта нет:

Ответ:
NONE

Отвечай только фактом или NONE.
"""
            },
            {
                "role": "user",
                "content": user_text
            }
        ]
    )

    fact = response.message.content.strip()

    if "\n" in fact:
        return "NONE"

    if len(fact) > 120:
        return "NONE"

    return fact