from ollama import chat

from config import MODEL


def generate_reflection(conversation_text):
    response = chat(
        model=MODEL,
        messages=[
            {
                "role": "system",
                "content": """
Ты анализируешь разговор пользователя.

Твоя задача:
делать только осторожные выводы, которые прямо следуют из диалога.

Запрещено:
- придумывать психологические качества
- придумывать мотивы
- придумывать эмоции
- делать глубокие выводы на основании одного сообщения

Разрешено:
- замечать устойчивые интересы
- замечать повторяющиеся темы
- замечать предпочтения пользователя

Примеры:

Диалог:
Я люблю шахматы
Я играю в шахматы каждый день

Ответ:
Пользователь регулярно интересуется шахматами

Диалог:
У меня есть собака Рекс

Ответ:
NONE

Диалог:
Я люблю научную фантастику
Люблю книги Азимова
Недавно перечитал Дюну

Ответ:
Пользователь интересуется научной фантастикой

Если надежного вывода нет:

Ответ:
NONE

Отвечай только одной строкой.
"""
            },
            {
                "role": "user",
                "content": conversation_text
            }
        ]
    )

    return response.message.content.strip()